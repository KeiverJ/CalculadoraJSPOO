    private void btnIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIgualActionPerformed
        this.num2 = Double.parseDouble(this.txtOperacion.getText());
        double resultado = 0;
        switch (this.operador){
            case "+": 
                resultado = operaciones.sumar(num1, num2);
                this.txtOperacion.setText(Double.toString(resultado));
                break;
            case "*": 
                resultado = operaciones.multiplicar(num1, num2);
                this.txtOperacion.setText(Double.toString(resultado));
                break;
            case "-": 
                resultado = operaciones.restar(num1, num2);
                this.txtOperacion.setText(Double.toString(resultado));
                break;
            case "/": 
                if (num2 == 0) {
                    this.txtOperacion.setText("ERROR");
                } else {
                    resultado = operaciones.dividir(num1, num2);
                    this.txtOperacion.setText(Double.toString(resultado));
                }
                break;
            case "^": 
                resultado = operaciones.calcularPotencia(num1, num2);
                this.txtOperacion.setText(Double.toString(resultado));
                break;
            case "%":
                resultado = operaciones.calcularPorcentaje(num1, num2);
                this.txtOperacion.setText(Double.toString(resultado));
                break;
        }
        resultadoMostrado = true;
    }//GEN-LAST:event_btnIgualActionPerformed

